#include <iostream>
#include <string>

// Pass by reference offers a way to avoid making an expensive copy of an argument when calling a function 
void printValue(std::string& y) // type changed to std::string&
{
    std::cout << y << '\n';
} // y is destroyed here

int main()
{
    std::string x{ "Hello, world!" };

    printValue(x); // x is now passed by reference into reference parameter y (inexpensive)

    return 0;
}

